#include <iostream>

using namespace std;

int main() {
   // Data Types : data types are declarations for variables. This determines the type and size of data associated with variables.

   // int : integer : 2 or 4 bytes
   int age = 13;

   // float : floating point : 4 bytes
   float area = 64.74;

   // double : double floating point : 8 bytes
   double volume = 134.64534;

   // char : charater : 1 bytes
   char test = 'h';

   //  wchar_t : wide character : 2 bytes
   wchar_t test2 = L'◊ù';

   // bool : boolean : 1 bytes
   bool isAdult = true;

   //! Type Modifiers :  type modifiers are used to change the meaning of the fundamental data types.

   /*
   signed int           : 4 bytes
   unsigned int         : 4 bytes
   short                : 2 bytes
   long                 : 4 bytes
   unsigned long        : 4 bytes
   long long            : 8 bytes
   unsigned long long   : 8 bytes
   long double          : 8 bytes
   signed char          : 1 bytes
   unsigned char        : 1 bytes
   */

   return 0;
}